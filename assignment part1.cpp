#include <iostream>
using namespace std;
int main ()
{
	char charType;
	int integerType;
	float floatType;
	double doubleType;
	
	cout<<"Size of char:"<<sizeof(charType)<<" bytes"<<"\n";
	cout<<"Size of int:"<<sizeof(integerType)<<" bytes"<<"\n";
	cout<<"Size of float:"<<sizeof(floatType)<<" bytes"<<"\n";
	cout<<"Size of double:"<<sizeof(doubleType)<<" bytes"<<"\n";
	
	return 0;
}
